public class Add {
   public static int addTwoNums(int x, int y, int z) {
      return x + y + z;
   }

   public static void main(String[] args) {
      System.out.println(Add.addTwoNums(3,5, 6));
      System.out.println(Add.addTwoNums(3,7, 8));
      System.out.println(Add.addTwoNums(3,7, 9));
   }
}
